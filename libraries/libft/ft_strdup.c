#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dst;
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	dst = (char *)src;
	while (src[j])
		j++;
	if (!(dst = malloc(j + 1)))
		return (0);
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
